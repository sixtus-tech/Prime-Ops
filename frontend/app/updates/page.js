// MAIN STATUS UPDATES PAGE
// ═══════════════════════════════════════════════════════════════════════

export default function StatusUpdatesPage() {
  const { authFetch } = useAuth();
  const [events, setEvents] = useState([]);
  const [loading, setLoading] = useState(true);
  const [selectedEvent, setSelectedEvent] = useState(null);
  const [consolidating, setConsolidating] = useState(null); // eventId being consolidated
  const [report, setReport] = useState(null);

  const fetchEvents = useCallback(async () => {
    try {
      const res = await fetch(`${API}/events`);
      const data = await res.json();
      // Only events that have committees
      setEvents((data.events || []).filter((e) => e.committees?.length > 0));
      if (!selectedEvent && data.events?.length > 0) {
        const first = data.events.find((e) => e.committees?.length > 0);
        if (first) setSelectedEvent(first.id);
      }
    } catch (err) {
      console.error(err);
    } finally {
      setLoading(false);
    }
  }, [selectedEvent]);

  useEffect(() => { fetchEvents(); }, [fetchEvents]);

  async function handleConsolidate(eventId) {
    setConsolidating(eventId);
    setReport(null);
    try {
      const data = await authFetch("/status-updates/consolidate", {
        method: "POST",
        body: JSON.stringify({ eventId }),
      });
      setReport(data.report);
    } catch (err) {
      alert("Failed to consolidate: " + err.message);
    } finally {
      setConsolidating(null);
    }
  }

  const currentEvent = events.find((e) => e.id === selectedEvent);

  if (loading) {
    return (
      <main className="min-h-screen bg-surface-50 p-8 flex items-center justify-center">
        <div className="w-6 h-6 border-2 border-brand-500 border-t-transparent rounded-full animate-spin" />
      </main>
    );
  }

  return (
    <main className="min-h-screen bg-surface-50">
      <div className="max-w-5xl mx-auto px-8 py-8 space-y-6">
        {/* Page header */}
        <div>
          <h1 className="font-display text-2xl text-surface-900">Status Updates</h1>
          <p className="text-surface-500 text-sm mt-1">
            View all committee progress reports and generate consolidated project-wide summaries.
          </p>
        </div>

        {/* Event selector */}
        {events.length > 0 ? (
          <div className="flex items-center gap-3 flex-wrap">
            {events.map((ev) => (
              <button
                key={ev.id}
                onClick={() => { setSelectedEvent(ev.id); setReport(null); }}
                className={`px-4 py-2 rounded-xl text-sm font-medium transition-all ${
                  selectedEvent === ev.id
                    ? "bg-brand-500 text-white shadow-sm"
                    : "bg-white text-surface-600 border border-surface-200 hover:border-brand-300"
                }`}
              >
                {ev.title}
                <span className="ml-1.5 text-xs opacity-70">({ev.committees?.length || 0})</span>
              </button>
            ))}
          </div>
        ) : (
          <div className="bg-white rounded-xl border border-surface-200 p-10 text-center">
            <p className="text-surface-400">No events with committees found.</p>
          </div>
        )}

        {/* Selected event content */}
        {selectedEvent && (
          <>
            {/* Consolidate button */}
            <div className="flex items-center justify-between bg-white rounded-xl border border-surface-200 p-4">
              <div>
                <h2 className="font-display text-lg text-surface-900">{currentEvent?.title}</h2>
                <p className="text-xs text-surface-400 mt-0.5">
                  {currentEvent?.committees?.length || 0} committees
                </p>
              </div>
              <button
                onClick={() => handleConsolidate(selectedEvent)}
                disabled={consolidating === selectedEvent}
                className="bg-gradient-to-r from-brand-500 to-brand-600 hover:from-brand-600 hover:to-brand-700 disabled:from-surface-300 disabled:to-surface-300 text-white font-medium px-5 py-2.5 rounded-xl transition-all flex items-center gap-2 shadow-sm"
              >
                {consolidating === selectedEvent ? (
                  <>
                    <div className="w-4 h-4 border-2 border-white border-t-transparent rounded-full animate-spin" />
                    Prime Ops is consolidating...
                  </>
                ) : (
                  <>
                    <svg width="18" height="18" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2">
                      <path d="M12 2v4M12 18v4M4.93 4.93l2.83 2.83M16.24 16.24l2.83 2.83" />
                    </svg>
                    Consolidate All Updates
                  </>
                )}
              </button>
            </div>

            {/* Consolidated report */}
            {report && <ConsolidatedReport report={report} onClose={() => setReport(null)} />}

            {/* Individual updates */}
            <div>
              <h3 className="font-display text-base text-surface-900 mb-4">Individual Committee Updates</h3>
              <StatusUpdateTimeline eventId={selectedEvent} isDirector />
            </div>
          </>
        )}
      </div>
    </main>
  );
}
